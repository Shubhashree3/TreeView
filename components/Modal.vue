<script>
export default {
  props: ['modalOpen', 'dataModal'],
  created() {
    console.log('modalData===', this.dataModal);
    console.log('modalOpen===', this.modalOpen);
    return this.openModel;
  },
  data() {
    return {
      tree: {
        id: '',
        label: '',
        parentId: '',
      },
    };
  },
  computed: {
    model: {
      get() { return this.modalOpen; },
